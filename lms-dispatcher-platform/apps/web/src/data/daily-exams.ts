// ── Types ─────────────────────────────────────────────────────────────────────
export type QType = 'regular' | 'case' | 'image';
export type VisualType =
  | 'supply_chain' | 'rpm_breakdown' | 'timezone_chart' | 'freight_zones'
  | 'dry_van_specs' | 'equipment_compare' | 'rate_con_doc' | 'bol_doc'
  | 'load_board_ui' | 'flatbed_specs';

export interface DailyOption {
  id: string;
  text: string;
  correct: boolean;
}
export interface DailyQuestion {
  id: string;
  chapter: number;
  type: QType;
  text: string;
  scenario?: string;
  visual?: VisualType;
  options: DailyOption[];
  explanation: string;
}
export interface DailyExamDef {
  day: number;
  questionIds: string[];
}

// ── Helper ─────────────────────────────────────────────────────────────────────
function q(
  id: string, chapter: number, type: QType,
  text: string,
  opts: [string, boolean][],
  explanation: string,
  extra?: { scenario?: string; visual?: VisualType },
): DailyQuestion {
  return {
    id, chapter, type, text, explanation,
    scenario: extra?.scenario,
    visual: extra?.visual,
    options: opts.map(([t, c], i) => ({ id: `${id}_${i}`, text: t, correct: c })),
  };
}

